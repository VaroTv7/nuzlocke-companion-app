export const getEnglishMoveName = (spanishName: string): string => {
    const clean = spanishName.toLowerCase().trim().replace(/ /g, '-').normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return MOVE_TRANSLATIONS[clean] || spanishName;
};

export const getSpanishMoveList = (): string[] => {
    return Object.keys(MOVE_TRANSLATIONS);
};
